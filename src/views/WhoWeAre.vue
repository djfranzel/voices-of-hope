          University of Minnesota as well as Twin Cities choir Magnum Chorum. On June 18, 2016, <b>Voices of Hope</b>
          performed their first full concert in the prison visiting room for an audience of incarcerated women. Over
          the next four years, <b>Voices of Hope</b> grew to nearly 50 singers, lengthening their rehearsal time to 1.5
          hours each Sunday afternoon. They have continued small and large collaborations with groups such as the
          University of Minnesota Women’s Chorus, Lumina Women’s Ensemble, Cantus Vocal Ensemble, 29:11 South
          African Gospel Ensemble, and Wonderlust Productions. </p>
        <p>In May 2017, <b>Voices of Hope</b> became a Limited Liability Corporation, fiscally sponsored by Springboard for
          the Arts. They are currently working towards becoming a 501c3 nonprofit organization. Their primary source
          of funding comes from individual donations; all money raised goes to support programming through staffing
          and supplies.</p>
